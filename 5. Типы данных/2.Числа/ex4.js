let i = 0;
while (i != 10) {
  i += 0.2;
}
// Этот цикл – бесконечный. Он никогда не завершится, почему?
// потому что во время выполнения цикла условие не выполнится, в данном случае i никогда не будет равно 10 
//( из за потери точности при работе с десятичными дробными числами)
// 9.799999999999997
// 9.999999999999996
// 10.199999999999996
// 10.399999999999995